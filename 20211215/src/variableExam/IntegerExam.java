package variableExam;

public class IntegerExam {

	public static void main(String[] args) {
		/* 정수형 int long short byte 
        
        * byte < short < int < long
        *   1      2      4     8   각각 2배씩 증가
        *
        * overflow : 작은 형 값은 큰 형으로는 넣을수 있지만 큰 형 값은 작은 형에는 넣을수 없다. 
        *
        *
        *
        */
		
		short sMin = -32768;
		short sMax = 32767;
		
		System.out.println("sMin : " + sMin);
		System.out.println("sMax : " + sMax);
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
